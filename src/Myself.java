public class Myself {
	public static void main(String[] args) {

		int i = 1;
		
		System.out.println("6단");
		
		while ( i < 10) {
			System.out.println("6 * "+ i +  " = " + 6 * i);
			i++;
		}

		System.out.println("7단");
		
		for ( int j = 1; j < 10; j++) {
			System.out.println("7 * " + j + " = " + 7 * j);
		}
	}
}
